# Инициализируем массив, где F[i] будет хранить значение F(i)
n_max = 2024  # Максимальное значение n, до которого нужно вычислить F
F = [0] * (n_max + 1)  # Список для хранения значений функции

# Условия по алгоритму
F[1] = 1
F[2] = 2

# Заполнение массива значениями функции для всех n > 2
for n in range(3, n_max + 1):
    F[n] = n * (n - 1) + F[n - 1] - F[n - 2]

# Рассчитываем выражение F(2024) + F(2020) - F(2019)
result = F[2024] + F[2020] - F[2019]

# Выводим результат
print("Результат выражения F(2024) + F(2020) - F(2019):", result)
