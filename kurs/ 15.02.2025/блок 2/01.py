# Перебираем все четырёхзначные числа от 1000 до 9999
for num in range(1000, 10000):
    # Разбиваем число на цифры
    a = num // 1000
    b = (num // 100) % 10
    c = (num // 10) % 10
    d = num % 10

    # Суммируем первую и последнюю цифры, вторую и третью
    sum1 = a + d
    sum2 = b + c

    # Если полученные суммы соответствуют 1 и 2, то выводим число
    if sum1 == 2 and sum2 == 1:
        print(f"Наименьшее число: {num}")
        break
