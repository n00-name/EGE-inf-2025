def process_number(N):
    # Шаг 1: проверка делимости на 3
    if N % 3 == 0:
        N //= 3
    else:
        N -= 1

    # Шаг 2: проверка делимости на 7
    if N % 7 == 0:
        N //= 7
    else:
        N -= 1

    # Шаг 3: проверка делимости на 11
    if N % 11 == 0:
        N //= 11
    else:
        N -= 1

    return N


# Найдем все такие числа N, для которых результат равен 6
count = 0
for N in range(2, 10000):  # 10000 можно заменить на любой подходящий диапазон
    if process_number(N) == 6:
        count += 1

print(f"Количество чисел N, при которых результат R = 6: {count}")
