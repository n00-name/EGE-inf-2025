# Функция для получения числа Y по правилам
def get_number(Y):
    # Первая цифра числа Y (остаток от деления на 4)
    digit1 = Y % 4
    # Вторая цифра числа Y (остаток от деления на 2)
    digit2 = Y % 2
    # Третья цифра числа Y (остаток от деления на 3)
    digit3 = Y % 3
    # Собираем число
    return int(f"{digit1}{digit2}{digit3}")

# Ищем минимальное двузначное число X, результат обработки которого равен 311
for X in range(10, 100):
    if get_number(X) == 311:
        print(f"Минимальное число X: {X}")
        break
